
      SUBROUTINE NUM_TO_CHAR(COUNT,CCOUNT)

      USE AMHGLOBALS,  ONLY:SO
      IMPLICIT NONE
      INTEGER COUNT,I,J,K
      CHARACTER CCOUNT*10,C(10)

      CCOUNT=' '



      IF (IABS(COUNT).GT.99999999) THEN
        WRITE(SO,*) 'NUMBER TOO LARGE TO CONVERT',COUNT
        STOP
      ENDIF

      J=0
      DO WHILE(COUNT.NE.0)
        J=J+1
        I=MOD(COUNT,10)
        C(J)=CHAR(I+48)
        COUNT=(COUNT-I)/10
      ENDDO

      DO K=1,J
        CCOUNT(J-K+1:J-K+1)=C(K)
      ENDDO



      RETURN
      END

