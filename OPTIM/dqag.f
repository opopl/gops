C
C     DESCRIPTION OF PARAMETERS
C      --INPUT--
C        IWHICH - INDEX OF ITEM DESIRED.
C                = 1 REFERS TO CURRENT ERROR NUMBER.
C                = 2 REFERS TO CURRENT ERROR CONTROL FLAG.
C                = 3 REFERS TO CURRENT UNIT NUMBER TO WHICH ERROR
