      PROGRAM MAKEXYZ
      LOGICAL YESNO

C     READ(*,*)
      INQUIRE(FILE='VECTOR.DUMP',EXIST=YESNO)
      IF (YESNO) OPEN(UNIT=8,FILE='VECTOR.DUMP',STATUS='OLD')
C     BTOA=0.529177D0
      BTOA=1.0D0
      DO J1=1,16000
         REWIND(8)
         READ(*,*,END=20) X, Y, Z
         IF (YESNO) READ(8,*) XV
         IF (YESNO) READ(8,*) XV, YV, ZV
         PRINT*,'38'
         PRINT*
         DO J2=1,38
            WRITE(*,10) 'LA ',X*BTOA,Y*BTOA,Z*BTOA,XV, YV, ZV
10          FORMAT(A2,6F20.10)
            IF (J2.NE.38) THEN
               READ(*,*,END=20) X, Y, Z
               IF (YESNO) READ(8,*) XV, YV, ZV
            ENDIF
         ENDDO
      ENDDO

20    STOP
      END
