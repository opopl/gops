         ENDIF
         GOTO 30
      ELSE
         NRUNNING2=NRUNNING2+RBNINGROUP(J2)
      ENDIF
   ENDDO
   NRUNNING1=NRUNNING1+RBNINGROUP(J1)
ENDDO

