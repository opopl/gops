!
!     THIS SUBPROGRAM PERFORMS A SORT ON THE INPUT DATA AND
!     ARRANGES IT FROM SMALLEST TO BIGGEST. THE EXCHANGE-SORT
!     ALGORITHM IS USED.
!
SUBROUTINE RBSORT(N,A)
IMPLICIT NONE
INTEGER J1, L, N, J2
DOUBLE PRECISION A(*), TEMP

DO J1=1,N-1
   L=J1
   DO J2=J1+1,N
      IF (A(L).GT.A(J2)) L=J2
   ENDDO
   TEMP=A(L)
   A(L)=A(J1)
   A(J1)=TEMP
ENDDO

RETURN
END SUBROUTINE RBSORT

