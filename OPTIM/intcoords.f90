                    IF (PTEST) PRINT*, "DRAGGING SWAP IN SWAP3TONCE", A3, A4
                    RFNEW(3*(A3-1)+1:3*A3) = RF(3*(A4-1)+1:3*A4)
                    RFNEW(3*(A4-1)+1:3*A4) = RF(3*(A3-1)+1:3*A3)
                 ENDDO
              ENDIF
           ENDIF
