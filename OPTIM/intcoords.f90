         RF2(3*(C2-1)+1:3*C2)=RF(3*(MINSWAP(JJ,2)-1)+1:3*MINSWAP(JJ,2))
         RF2(3*(C3-1)+1:3*C3)=RF(3*(MINSWAP(JJ,3)-1)+1:3*MINSWAP(JJ,3))
         !PRINT*, "NEW CURINTDIST", MININTDIST
         CURINTDIST = MININTDIST
