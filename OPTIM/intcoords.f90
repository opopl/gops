               B2=PERMGROUP(ST+1); B3= PERMGROUP(ST+2)
            ELSEIF (I.EQ.1) THEN
               B2=C1; B3= PERMGROUP(ST+2)
            ELSE
               B2=PERMGROUP(ST+1); B3=C1
