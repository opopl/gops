C     THIS SUBPROGRAM PERFORMS A SORT ON THE INPUT DATA AND
C     ARRANGES IT FROM SMALLEST TO BIGGEST. THE EXCHANGE-SORT
C     ALGORITHM IS USED.
C
      SUBROUTINE GSORT2(N,NATOMS)
      USE COMMONS, ONLY : QMINAV, QMINPCSMAV, CSMT
      USE QMODULE
      IMPLICIT NONE
      INTEGER NATOMS
      INTEGER J1, L, N, J3, J2, NTEMP
      DOUBLE PRECISION TEMP, C
C
      DO 20 J1=1,N-1
         L=J1
         DO 10 J2=J1+1,N
            IF (QMIN(L).GT.QMIN(J2)) L=J2
10       CONTINUE
         TEMP=QMIN(L)
         QMIN(L)=QMIN(J1)
         QMIN(J1)=TEMP
         IF (CSMT) THEN
            TEMP=QMINAV(L)
            QMINAV(L)=QMINAV(J1)
            QMINAV(J1)=TEMP
         ENDIF
         NTEMP=FF(L)
         FF(L)=FF(J1)
         FF(J1)=NTEMP
         DO J2=1,3*NATOMS
            C=QMINP(L,J2)
            QMINP(L,J2)=QMINP(J1,J2)
            QMINP(J1,J2)=C
         ENDDO
         IF (CSMT) THEN
            DO J2=1,3*NATOMS
               C=QMINPCSMAV(L,J2)
               QMINPCSMAV(L,J2)=QMINPCSMAV(J1,J2)
               QMINPCSMAV(J1,J2)=C
            ENDDO
         ENDIF
20    CONTINUE
      RETURN
      END
