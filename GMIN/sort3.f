C     THIS SUBPROGRAM PERFORMS A SORT ON THE INPUT DATA AND
C     ARRANGES IT FROM SMALLEST TO BIGGEST. THE EXCHANGE-SORT
C     ALGORITHM IS USED.
C
      SUBROUTINE SORT3(N,J3,A,B)
      IMPLICIT NONE
      INTEGER J1, L, N, J3, J2
      DOUBLE PRECISION TEMP, A(J3), B(3*J3)
C
      DO 20 J1=1,N-1
         L=J1
         DO 10 J2=J1+1,N
            IF (A(L).LT.A(J2)) L=J2
10       CONTINUE
         TEMP=A(L)
         A(L)=A(J1)
         A(J1)=TEMP
         DO J2=0,2
            TEMP=B(3*L-J2)
            B(3*L-J2)=B(3*J1-J2)
            B(3*J1-J2)=TEMP
         ENDDO
20    CONTINUE
      RETURN
      END
