                   WRITE(1979,'(3G20.10)') CURRENTPOINTENERGY, DIHEORDERPARAM, SASAORDERPARAM
                ELSEIF (OSASAT) THEN
                   WRITE(1979,'(2G20.10)') CURRENTPOINTENERGY, SASAORDERPARAM
