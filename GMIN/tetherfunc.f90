      REAL(8) FUNCTION GETDISPLACEMENT(STEP)
           IMPLICIT NONE
           REAL(8),INTENT(IN) :: STEP
           REAL(8) :: HARVEST
           CALL RANDOM_NUMBER(HARVEST)
           GETDISPLACEMENT=2.0D0*HARVEST*STEP-STEP
      END FUNCTION GETDISPLACEMENT

