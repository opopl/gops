		WRITE(1980+NODE,'(1G20.10, I10)') BINENERGIES(JB), BINENIMPORTANCEINDEX(JB,NODE)
		ENDDO
		CLOSE (1980+NODE)
	 ENDIF
 
         IF (BINENSAVED.EQ.SIZE(BINENERGIES)) THEN
