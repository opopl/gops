!> \BRIEF THIS SUBROUTINE ...
!> \NAME MCRUNS
!> \PARAM SCREENC  
!OP226>=================================== 
      SUBROUTINE MCRUNS(SCREENC)
      USE COMMONS
      IMPLICIT NONE
      LOGICAL LOPEN
      DOUBLE PRECISION SCREENC(3*NATOMS)

      INTEGER J1

      IF (PTMC.OR.BSPT) THEN
         CALL PTBASINSAMPLING
         RETURN
      ENDIF

      INQUIRE(UNIT=1,OPENED=LOPEN)
      IF (LOPEN) THEN
         WRITE(*,'(A,I2,A)') 'MCRUNS> ERROR *** UNIT ', 1, ' IS NOT FREE '
         STOP
      ENDIF
C
C  NRUNS > 1 IS AN OBSOLETE OPTION! DJW
C
      DO J1=1,NRUNS
         CALL MC(MCSTEPS(J1),TFAC(J1),SCREENC)
      ENDDO

      CALL FINALQ
      CALL FINALIO

      RETURN
      END
