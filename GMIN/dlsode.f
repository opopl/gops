C     CODE FOR EQUAL, POSITIVE, NON-UNIT INCREMENTS.
C
   60 NS = N*INCX
      DO 70 I = 1,NS,INCX
        DY(I) = DA*DX(I) + DY(I)
   70 CONTINUE
      RETURN
      END
